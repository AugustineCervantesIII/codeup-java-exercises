public class Animal {

    String hunting;
    String chasing;
    String feasting;
    String name;
    String color;
    String activity;

    public Animal(String hunting, String chasing, String feasting, String name, String color, String activity){
        this.hunting = hunting;
        this.chasing = chasing;
        this.feasting = feasting;
        this.name = name;
        this.color = color;
        this.activity = activity;
    }


}
